package com.topacademy.springapp;

public class Engine {
    private int numberHorsepower;
    private float volume;
    private float weight;
    private float price;

    public int getNumberHorsepower() {
        return numberHorsepower;
    }

    public float getVolume() {
        return volume;
    }

    public float getWeight() {
        return weight;
    }

    public float getPrice() {
        return price;
    }

    public void setNumberHorsepower(int numberHorsepower) {
        this.numberHorsepower = numberHorsepower;
    }

    public void setVolume(float volume) {
        this.volume = volume;
    }

    public void setWeight(float weight) {
        this.weight = weight;
    }

    public void setPrice(float price) {
        this.price = price;
    }
}
